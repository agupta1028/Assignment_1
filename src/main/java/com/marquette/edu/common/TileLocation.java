package com.marquette.edu.common;

public class TileLocation {
	
	int xcord;
	int ycord;
	
	public TileLocation(int xcord, int ycord) {
		this.xcord = xcord;
		this.ycord = ycord;
	}
	
	public int getXcord() {
		return xcord;
	}
	public void setXcord(int xcord) {
		this.xcord = xcord;
	}
	public int getYcord() {
		return ycord;
	}
	public void setYcord(int ycord) {
		this.ycord = ycord;
	}

}
